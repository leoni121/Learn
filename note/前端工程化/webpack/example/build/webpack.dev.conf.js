'use strict'
const utils = require('./utils')
const webpack = require('webpack')
const devConfig = require('./config').dev
const merge = require('webpack-merge')
const path = require('path')
const baseWebpackConfig = require('./webpack.base.conf')
const AddAssetHtmlPlugin = require('add-asset-html-webpack-plugin')
const HtmlWebpackPlugin = require('html-webpack-plugin')
const FriendlyErrorsPlugin = require('friendly-errors-webpack-plugin')
const portfinder = require('portfinder')

const HOST = process.env.HOST
const PORT = process.env.PORT && Number(process.env.PORT)

function resolve (dir) {
  return path.join(__dirname, '..', dir)
}

const devWebpackConfig = merge(baseWebpackConfig, {
  mode: 'development',
  // 此选项控制是否以及如何生成source-map。cheap-module-eval-source-map is faster for development
  devtool: devConfig.devtool,

  // 这是开发服务配置，包括热替换什么的 依赖  ./config.js
  devServer: {
    // 当使用内联模式(inline mode)时，控制台(console)将显示消息，可能的值有 none, error, warning 或者 info（默认值）。
    clientLogLevel: 'warning', 
    // 当使用 HTML5 History API 时，任意的 404 响应都可能需要被替代为 index.html
    historyApiFallback: {
      rewrites: [
        { from: /.*/, to: path.posix.join(devConfig.assetsPublicPath, 'index.html') },
      ],
    },
    // 启用 webpack 的模块热替换特性
    hot: true,
    // 告诉服务器从哪里提供内容。只有在你想要提供静态文件时才需要。我们这里直接禁用掉
    contentBase: path.join(__dirname, "dist"), // 和 contentBase: false 一样的效果
    // 一切服务都启用gzip 压缩：
    compress: true,
    // 指定使用一个 host。默认是 localhost
    host: HOST || devConfig.host,
    // 指定要监听请求的端口号
    port: PORT || devConfig.port,
    // dev服务器自动打开浏览器。
    open: devConfig.autoOpenBrowser,
    // 当出现编译器错误或警告时，在浏览器中显示全屏遮罩层。默认情况下禁用。
    overlay: devConfig.errorOverlay
      ? { warnings: false, errors: true }
      : false,
    // 浏览器中访问的相对路径
    // publicPath: devConfig.assetsPublicPath,
    // 代理配置
    proxy: devConfig.proxyTable,
    // 除了初始启动信息之外的任何内容都不会被打印到控制台。这也意味着来自 webpack 的错误或警告在控制台不可见。
    // 闭所有错误日志记录,使用我们配置的 FriendlyErrorsPlugin 来显示错误信息到控制台
    quiet: true,
    //webpack 使用文件系统(file system)获取文件改动的通知。在某些情况下，不会正常工作。例如，当使用 Network File System (NFS) 时。Vagrant 也有很多问题。在这些情况下，请使用轮询。
    // https://www.webpackjs.com/configuration/watch/
    watchOptions: {
      poll: devConfig.poll
    }
  },
  plugins: [
    // 启用模块热替换(HMR)
    new webpack.HotModuleReplacementPlugin(),
    
    /*  // 当开启 HMR 的时候使用该插件会显示模块的相对路径，建议用于开发环境。
    new webpack.NamedModulesPlugin(),
    // 在编译出现错误时，使用 NoEmitOnErrorsPlugin 来跳过输出阶段。这样可以确保输出资源不会包含错误。
    new webpack.NoEmitOnErrorsPlugin(), -- 生产环境
    */
    
    // 生产html文件
    new HtmlWebpackPlugin({
      filename: 'index.html',
      template: 'index.html',
      inject: true
    }),

    // This will add a script tag to the HTML generated by html-webpack-plugin
    new AddAssetHtmlPlugin({
      filepath: path.resolve(__dirname, '../dll/*.dll.js'),
      publicPath: devConfig.assetsPublicPath + 'static/js',
      outputPath: '../dist/static/js',
      includeSourcemap: false
    }),
  ]
})

// module 配置可以导出 一个对象、对象数组、Promise、函数
module.exports = new Promise((resolve, reject) => {

  // 设置默认基础PORT
  portfinder.basePort = process.env.PORT || devConfig.port
  // 当基础端口和当前电脑中端口重复时调用 获取一个新的端口，获取成功
  portfinder.getPort((err, port) => {
    if (err) {
      reject(err)
    } else {
      // 发布一个新的端口
      process.env.PORT = port
      // 新的端口添加到devServer中
      devWebpackConfig.devServer.port = port

      // 根据不同的port动态添加 FriendlyErrorsPlugin，得到不同的提示信息
      devWebpackConfig.plugins.push(
        new FriendlyErrorsPlugin({ //  DONE  Compiled successfully in 5131ms 

          // 成功
          compilationSuccessInfo: {
            messages: [`Your application is running here: http://${devWebpackConfig.devServer.host}:${port}`],
          },
          onErrors: devConfig.notifyOnErrors
          ? utils.createNotifierCallback()
          : undefined
        })
      )
      resolve(devWebpackConfig)
    }
  })
})
